    private void cmdBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdBorrarMouseClicked
        camisaNueva.setID(Integer.parseInt(this.txtID.getText()));
        //agrego a mi base de datos
        if(camisaNueva.borrarCamisa()){
            JOptionPane.showMessageDialog(null,"Se elimino la camisa de la base de datos");
            //Muestro el catalogo actualizado en la tabla
            this.TablaCamisas.setModel(catalogoNuevo.obtenerInformacion("camisa"));
        }else{
            JOptionPane.showMessageDialog(null,"No se pudo eliminar la camisa de la base de datos");
        }
        
    }//GEN-LAST:event_cmdBorrarMouseClicked

