    private void cmdModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdModificarMouseClicked
        JOptionPane.showMessageDialog(this,"hola si entra");
        camisaNueva.setID(Integer.parseInt(this.txtID.getText()));
        camisaNueva.setColor(this.txtColor.getText());
        camisaNueva.setDescripcion(this.txtDescripcion.getText());
        camisaNueva.setCantidad(Integer.parseInt(this.txtCantidad.getText())); 
        camisaNueva.setPrecio(Double.parseDouble(this.txtPrecio.getText()));
        camisaNueva.setTam(Integer.parseInt(this.txtTam.getText()));
        camisaNueva.setTemporada(this.txtTemporada.getText());
        
        if (camisaNueva.modificarCamisa()){
            JOptionPane.showMessageDialog(null,"Se modificaron los datos de la camisa");
            //Muestro el catalogo actualizado en la tabla
            this.TablaCamisas.setModel(catalogoNuevo.obtenerInformacion("camisa"));
        }
        else{
            JOptionPane.showMessageDialog(null,"No se pudo modificar los datos de la camisa ");
        }
        
        
    }//GEN-LAST:event_cmdModificarMouseClicked

